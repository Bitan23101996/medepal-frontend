import { Pipe, PipeTransform } from '@angular/core';

@Pipe({
  name: 'timeFormat'
})

export class TimeFormatPipe implements PipeTransform {
  transform(value: number): string {
    var parts = (<any>value).split(':');
    parts = parts.slice(0,-1).join(':');
    return parts;
  }
}
