import { Pipe, PipeTransform } from "@angular/core";

@Pipe({
  name: "sort",
  pure: true
})
export class SortNumberPipe  implements PipeTransform {
  transform(array: any, field: string, orderType: boolean): any[] {
   // console.log(array);
   // console.log(field);
    if (!Array.isArray(array)) {
      return;
    }
    array.sort((a: any, b: any) => {
      if (a[field] < b[field]) {
        return -1;
      } else if (a[field] > b[field]) {
        return 1;
      } else {
        return 0;
      }
    });
    return orderType ? array:array.reverse();
  }
}